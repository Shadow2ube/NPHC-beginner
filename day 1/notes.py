# int : number without decimal
a = 1

# char : a single character
b = 'a'

# string : a group of characters
c = 'abc'

# bool : true or false
d = True

# float : a number with a decimal point
e = 1.1

# complex : a group of numbers and letters (we won't be using them)
f = complex(1.1, '4c')

# + : add
b + c

# - : subtract
1 - e

# = : equal
a = b

# Logic

# > : greater than
a > b

# < : less than
c < d

# == : equal to
c == d

# >= : greater than or equal to
a >= d

# <= : less than or equal to
c <= b

# != : not equal to
a != b